def day_name(day):
    if day == 0:
        return "Sunday"
    elif day == 1:
        return "Monday"
    elif day == 2:
        return "Tuesday"
    elif day == 3:
        return "Wednesday"
    elif day == 4:
        return "Thursday"
    elif day == 5:
        return "Friday"
    elif day == 6:
        return "Saturday"


print(day_name(0))
print(day_name(1))
print(day_name(2))
print(day_name(3))
print(day_name(4))
print(day_name(5))
print(day_name(6))
